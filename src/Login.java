 private boolean isEmpty() {
    if (jtemail.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Email address is required", "Warning", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    if (!(jtemail.getText().matches("^.+@.+\\..+$"))) {
        JOptionPane.showMessageDialog(this, "Invalid Email address", "Warning", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    String password = String.valueOf(jtpassword.getPassword());
if (password.length() < 8 || password.length() > 16) {
    JOptionPane.showMessageDialog(this, "Password must be between 8 and 16 characters long", "Warning", JOptionPane.WARNING_MESSAGE);
    return false;
}
if (!password.matches(".*[A-Z].*")) {
    JOptionPane.showMessageDialog(this, "Password must contain at least one uppercase letter", "Warning", JOptionPane.WARNING_MESSAGE);
    return false;
}
if (!password.matches(".*[a-z].*")) {
    JOptionPane.showMessageDialog(this, "Password must contain at least one lowercase letter", "Warning", JOptionPane.WARNING_MESSAGE);
    return false;
}
if (!password.matches(".*\\d.*")) {
    JOptionPane.showMessageDialog(this, "Password must contain at least one digit", "Warning", JOptionPane.WARNING_MESSAGE);
    return false;
}
// Additional validation for special characters if needed

return true;


}


    
    
   
   
